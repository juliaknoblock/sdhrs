        <h2 class="head2"><span>Boarding</span></h2>

        <p>San Diego House Rabbit Society offers boarding at the Adoption Center in Kearny Mesa.
        <p>Rabbits are boarded in a roomy exercise pen in Suite B. (The shelter rabbits are housed in Suite A.)

        <p><a href="../boardingcontract.pdf" target="_blank" id="pagelink">Download our Boarding Contract.</a>  Complete and bring at the time you drop-off your bunny(s).

        <p><center>Boarding fees:

        <p>Single Rabbit  $22.00 per night

        <p>Bonded Pair  $26.00 per night

        <p>Each additional rabbit in same pen  $5.00 per night

        <p>Expanded Pen   $5.00 per night

        <p>Additional Litter box  $2.00 per day

        <p>Long term Stays (over 2 weeks) - Per negotiation (contact boarding@sandiegorabbits.org)</center>



        <p>Additional Services:
<p>Administration of oral Medications $1 per med, per day
<p>Injectible Meds $5 per med, per day
<p>Sub-Q Fluids $5 per day
<p>Trip to Vet Office $20 per trip
<p>Nursing and Special Care $15 per day (wound cleaning, bandages changes, bathing, etc.)




        <p>Grooming:
<p>Nail Trim Only $10
<p>Nails w/Light Grooming $15
<p>Heavy Grooming $25
<p>Angora De-Matting $65



        <p>To make a reservation, call 858-565-2869 or email boarding@sandiegorabbits.org with the Subject line: "Bunny Boarding" or stop by the SDHRS Bunny Store in Kearny Mesa.
<p>50% of the boarding fee is required to hold your reservation. The remainder of total fee is due at the time of drop-off.
<p>Payments may be made in the SDHRS Bunny Store, 4807 Mercury Street, Suite A, SD 92111, (Wednesday-Friday  noon to 5:30 p.m., Saturday 11 a.m. to 4 p.m., and Sunday, Noon to 4 p.m.) by cash, check or credit card.





