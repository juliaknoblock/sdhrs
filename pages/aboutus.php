		<h2 class="head2"><span>HRS Philosophy</span></h2>
			<p>As a chapter of the international House Rabbit Society, San Diego House Rabbit Society adheres to this philosophy. 

			<p>- The House Rabbit Society (HRS) believes that ALL rabbits are valuable as individuals, regardless of breed purity, temperament, state of health, or relationship to humans. The welfare of all rabbits is our primary consideration. 

			<p>- Except for unique situations, in which wild animals are being nursed or rehabilitated, it is in the best interest of wild rabbits that human intervention be held to a minimum. 

			<p>- Domestic rabbits are not the product of natural selection, but rather of human interference by means of breeding programs, and the product is a human-dependent animal who needs protection. It is therefore a human responsibility that these animals be cared for in a manner appropriate to their needs. 

			<p>- It is in the best interest of domestic rabbits to be neutered or spayed, to live indoors in the home where supervision and protection are provided, and to be treated for illnesses by rabbit-experienced veterinarians. 

			<p>- Domestic rabbits are companion animals and should be afforded at least the same individual rights, level of care, and opportunity for longevity as is commonly given to dogs and cats who live as human companions. 

			<p>- Rabbits are intelligent, social animals, who require mental stimulation, toys, exercise, environmental activity and social interaction from, as appropriate, people, other rabbits, or other animals. 

