		<h2 class="head2"><span>Who We Are &amp; What We Do</span></h2>
			<p>We are a 501(c)(3) nonprofit charitable organization and our federal identification number is 65-1262427. Please use our legal name, San Diego Companion Rabbit Society, Inc. and this ID number when making any charitable contributions to our organization. Your donations are tax-deductible to the extent allowed by law. 

			<p>San Diego House Rabbit Society (SDHRS) is a chapter of the international House Rabbit Society (based in Richmond, CA) and has been at work in San Diego and southwest Riverside counties for more than 20 years. We celebrated our 20th Anniversary in 2012. 

			<p>We have a satellite rescue associated with our chapter, Lucky Bunny Rabbit Rescue, based in Murrieta, CA and serving the communities of Wildomar, Lake Elsinore, Murrieta, Temecula and surrounding areas in southwest Riverside county. This rescue group partners with and supports the shelter, Animal Friends of the Valleys, in Wildomar, CA. 

			<p>Our Mission is to rescue abandoned domestic rabbits within our community and find them new homes, support our local shelters in the care of their rabbits, and educate the community on the proper and responsible care of domestic rabbit companions. 

			<p>SDHRS is an volunteer-driven, nonprofit rabbit welfare organization that does the following: 

			<p>Supports local shelters and humane societies with spay/neuter services for their rabbits 

			<p>Provides medical care for shelter rabbits that is beyond the capability of that shelter's medical facility 

			<p>Assists shelters by taking in rabbits for fostering when the rabbit has a medical or behavioral problem not able to be managed by the shelter 

			<p>Provides equipment and supplies to shelters not having the funding to adequately provide for their rabbits' needs 

			<p>Partners with local shelters and humane societies to promote rabbits for adoption and to educate the community and responsible rabbit care 

			<p>Supports the general community through our hotline, Bunny Supply Store, and educational seminars to help them care for their rabbit companions 

			<p>Provides referrals to lower cost spay/neuter services and provides a financial incentive to individuals getting their rabbit altered 

			<p>Provides a "rabbit community" environment of social activities and volunteer opportunities for those interested in rabbits as indoor family companions 

			<p>SDHRS has supported local shelters and our community for over 22 years! 

