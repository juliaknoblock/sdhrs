            <h2 class="head2"><span>neat idea cages (nic)</span></h2>

            <p>Neat Idea Cages (NIC) are handmade cages created with supplies you can buy at local stores. The frame is usually composed of wire bookshelf panels (sold <a href="http://www.target.com/p/wire-cube-shelving-system/-/A-10882811#prodSlot=medium_1_3&term=wire+bookshelf" target="_blank" id="pagelink">here</a> from Target.com). Be sure to separate the large holes from the smaller ones, as it is possible for a rabbit to get his or her head stuck inside. Floors can be made from cardboard, covered in carpeting and reinforced with wood and wooden dowel rods. Attach each panel with zip ties and cut them short so bunny is not able to eat them. The NIC is for the creative type - please send us your creations to share with other bunny owners!</p>
            
