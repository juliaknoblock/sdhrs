            <h2 class="head2"><span>creative cage ideas</span></h2>

            <p>Smaller rabbits can do well in a cage as long as they are given at least three hours outside their cage, every day. They need room to run and play and interact with you. No matter how large a cage may be, time outside of the cage (or pen) is very important.
<p>Make sure you purchase a cage that is large enough to accommodate Bunny when he/she is full grown. Make sure the cage is large enough for bunny to lay stretched out, when all the necessary supplies (litter box, water & food bowls) are inside. 
<p>Take into consideration the amount of daily run time your rabbit will receive, when determining cage size; in this case, bigger is better. 
<p>Make sure the cage door is large enough for a large cat litter box to fit through. 
<p>Make sure the cage door opens from the side, and not down, so bunny's feet won't get caught when entering or exiting the cage. 
<p>Urine guards are helpful in keeping hay and urine in the cage. 
<p>Casters or wheels on the cage make it easy to move about when needing to clean bunny's area, 
<p>And, don't forget, bunny needs several hours "out of cage" time, each day!
<p>For small to medium rabbits (2-5 lbs.) we recommend a cage no smaller than 42" wide x 30" deep x 24" high. If bunny is larger, then a minimum of 36" wide x 30" deep is better (best of all, however, is the x-pen setup). 

