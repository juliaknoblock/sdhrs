        <h2 class="head2"><span>x-pen living</span></h2>

        <p>Housing your rabbit in an exercise pen (commonly known as an x-pen) can be a big improvement over the idea of a cage. With a cage your rabbit's space can be limited and he can become frustrated at being in a smaller space when you are away or asleep at night. X-pen living gives bunny more space, while still keeping him safely contained, during those times when you are not at home or need to keep bunny enclosed. Even for "jumpers" x-pens can be a good choice, simply by attaching some shade cloth (found in garden centers) or special-made wire tops made by pen manufacturers.
<p>Bunny's x-pen not only holds his litter box, water and food dishes, but it can also hold a Cottontail Cottage, toys, and most anything he desires. What really makes them great is:
<p>1) You're getting more space for your money (x-pens cost the same or less than most rabbit cages). 
<p>2) Bunny has more room to roam and feels "freer" while still in a safe place. 
<p>3) Pens are flexible and can be configured in any shape to suit your available space. 
<p>4) They are easy to clean and portable.

